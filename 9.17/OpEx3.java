class OpEx3 {
	public static void main(String args[]){
		int num1 = 2;
		int num2 = 4;
		int num3 = 4;

		boolean result;

		result = num1 < num2;

		System.out.println(result);

		result = num1 > num2;
		System.out.println(result);

		result = num1 >= num2;
		System.out.println(result);

		result = num1 <= num2;
		System.out.println(result);

		result = num2 <= num3;
		System.out.println(result);
		
		result = num2 == num3;
		System.out.println(result);

	}
}